'Faça um programa que tendo como dados de entrada a distância total (em km) percorrida por um automóvel e a quantidade de combustível (em litros) consumida para percorrê-la, calcule e imprima o consumo médio de combustível'

dist = float(input("Digite a distância total (em km): "))
comb = float(input("Digite o consumo de combustível (em litros): "))

print("O consumo médio é de", dist/comb, "km/l.")